/************************************************************************************************/
KsTask::KsTask()
	: KsUpdate( 0 )
	, KsDraw( 0 )
{
}

/************************************************************************************************/
/*
 * コンストラクタ
 * @param	updateOrder		更新オーダー
 * @param	drawOrder		描画オーダー
 */
/************************************************************************************************/
KsTask::KsTask( KsUInt32 updateOrder, KsUInt32 drawOrder )
	: KsUpdate( updateOrder )
	, KsDraw( drawOrder )
{
}


/************************************************************************************************/
/*
 * デストラクタ
 */
/************************************************************************************************/
KsTask::~KsTask()
{
}

/************************************************************************************************/
/*
 * 破棄する(マネージャ側で削除する)
 */
/************************************************************************************************/
void KsTask::destroy()
{
	destroyUpdate();
	destroyDraw();
}

