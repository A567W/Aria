/************************************************************************************************/
/*
 * コンストラクタ
 */
/************************************************************************************************/
KsShaderResource::KsShaderResource()
{
	m_name      = "";
	m_id        = 0;
	m_flags     = 0;
}

/************************************************************************************************/
/*
 * デストラクタ
 */
/************************************************************************************************/
KsShaderResource::~KsShaderResource()
{
}

/************************************************************************************************/
/*
 * テクスチャ名をセットする
 * @param		pFileName			テクスチャ名
 */
/************************************************************************************************/
void KsShaderResource::setName( const KsChar* pFileName )
{
	m_name = pFileName;
	
	m_id = KsCrc32( (KsUInt8*)pFileName );
}


