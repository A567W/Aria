/************************************************************************************************/
/*
 * デフォルトブレンドステート
 */
/************************************************************************************************/
const KS_DEPTH_STENCIL_DESC KsDepthStencilState::DEFAULT = 
{
	ksTRUE,
	KS_DEPTH_WRITE_MASK_ALL,
	KS_COMPARISON_GREATER,
	ksFALSE,
	KS_DEFAULT_STENCIL_READ_MASK,
	KS_DEFAULT_STENCIL_WRITE_MASK,
	{ KS_STENCIL_OP_KEEP, KS_STENCIL_OP_KEEP, KS_STENCIL_OP_KEEP, KS_COMPARISON_ALWAYS },
	{ KS_STENCIL_OP_KEEP, KS_STENCIL_OP_KEEP, KS_STENCIL_OP_KEEP, KS_COMPARISON_ALWAYS }
};


/************************************************************************************************/
/*
 * コンストラクタ
 */
/************************************************************************************************/
KsDepthStencilState::KsDepthStencilState()
{
	KsZeroMemory( &m_depthStencilDesc, sizeof(KS_DEPTH_STENCIL_DESC) );
}

/************************************************************************************************/
/*
 * コンストラクタ
 */
/************************************************************************************************/
KsDepthStencilState::KsDepthStencilState( const KS_DEPTH_STENCIL_DESC* pDepthStencilDesc )
{
	KsMemCopy( &m_depthStencilDesc, pDepthStencilDesc, sizeof(KS_DEPTH_STENCIL_DESC) );
}

/************************************************************************************************/
/*
 * コンストラクタ
 */
/************************************************************************************************/
KsDepthStencilState::KsDepthStencilState( const KS_DEPTH_STENCIL_DESC& refDepthStencilDesc )
	: m_depthStencilDesc( refDepthStencilDesc )
{	
}

/************************************************************************************************/
/*
 * デストラクタ
 */
/************************************************************************************************/
KsDepthStencilState::~KsDepthStencilState()
{
}

